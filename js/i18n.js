let currentLang = "uz";

const languageFlags = {
  uz: "assets/flag/flaguz.svg",
  kk: "assets/flag/flagqr.svg",
  ru: "assets/flag/flagrus.svg",
  en: "assets/flag/flageng.svg"
};

const languageFlagAlt = {
  uz: "Uzbek flag",
  kk: "Qoraqalpoq flag",
  ru: "Russian flag",
  en: "English flag"
};

function applyLanguage(lang) {
  currentLang = translations[lang] ? lang : "uz";
  document.documentElement.lang = currentLang;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    const value = translations[currentLang][key];
    if (value) node.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.getAttribute("data-i18n-placeholder");
    const value = translations[currentLang][key];
    if (value) node.setAttribute("placeholder", value);
  });

  const currentFlag = document.querySelector("[data-current-flag]");
  if (currentFlag) {
    currentFlag.setAttribute("src", languageFlags[currentLang]);
    currentFlag.setAttribute("alt", languageFlagAlt[currentLang]);
  }
}

window.setLanguage = function setLanguage(lang) {
  localStorage.setItem("lang", lang);
  applyLanguage(lang);
};

document.addEventListener("DOMContentLoaded", () => {
  applyLanguage(localStorage.getItem("lang") || "uz");
});
